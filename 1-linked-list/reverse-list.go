package linked_list

//#linked_list

func ReverseList(head *ListNode) *ListNode {
	// reversedHead — это "новый список", который мы постепенно строим (изначально пустой)
	var reversedHead *ListNode

	// Пока не дошли до конца исходного списка
	for head != nil {
		// currentNext — сохраняем ссылку на следующий элемент,
		// потому что head.Next мы сейчас перезапишем
		currentNext := head.Next

		// Переписываем связь: теперь текущая нода указывает на начало уже перевёрнутой части
		head.Next = reversedHead

		// "Переносим голову" перевёрнутого списка вперёд:
		// теперь reversedHead — это текущая нода
		reversedHead = head

		// Двигаем указатель head к следующей (ещё не перевёрнутой) ноде
		head = currentNext

		// Альтернативное решение с параллельным присваиванием:
		/*
			Присваивание происходит в две фазы.
			Сначала вычисляются операнды выражений индексов и косвенных указателей
			(включая неявные косвенные указатели в селекторах) слева и выражения справа в обычном порядке.
			Затем присваивания выполняются слева направо.

			head.Next, reversedHead, head = reversedHead, head, head.Next
		*/
	}

	// Когда исходный список закончился — возвращаем начало перевёрнутого
	return reversedHead
}

